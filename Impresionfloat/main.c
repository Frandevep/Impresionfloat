#include <stdio.h>


int main()
{
    int num=10; //dar valor numerico a una variable, num es el vector de la variable int
    float numdecimal=2.33; //permite dar valor a una variable decimal

    printf("%d",num); // imprimir esa variable con valor numerico
    printf("\n");//genera salto de linea para visualizar mejor la impresion
    printf("%f",numdecimal); //imprime variable decimal


    return 0;
}
